def read_mini(mini):
	scores = []
	features = []
	features = mini.split('\n')
	for feature in features:
		splitted = feature.split(' ')
		score = splitted[0]
		name = splitted[1]
		scores.append(score)
		features.append(SNP(name))
	(scores,features)

